#ifndef SX1280_DEFINITIONS_H
#define SX1280_DEFINITIONS_H

// Platform uyumluluğu için include guard
#if ARDUINO >= 100
#include <Arduino.h>
#else
#include <WProgram.h>
#endif

// SX1280 Register Adresleri (örnekler)
#define SX1280_REG_CHIP_VERSION 0x091F
#define SX1280_REG_OPMODE 0x900
#define SX1280_REG_FIFO 0x800

// SX1280 Komutları
#define SX1280_CMD_SET_SLEEP 0x84
#define SX1280_CMD_SET_STANDBY 0x80
#define SX1280_CMD_SET_FS 0xC1
#define SX1280_CMD_SET_TX 0x83
#define SX1280_CMD_SET_RX 0x82
#define SX1280_CMD_WRITE_REGISTER 0x0D
#define SX1280_CMD_READ_REGISTER 0x1D
#define SX1280_CMD_WRITE_BUFFER 0x0E
#define SX1280_CMD_READ_BUFFER 0x1E

// Standby modları
#define SX1280_STANDBY_RC 0x00
#define SX1280_STANDBY_XOSC 0x01

// Opmode değerleri
#define SX1280_OPMODE_SLEEP 0x00
#define SX1280_OPMODE_STBY 0x01
#define SX1280_OPMODE_FS 0x02
#define SX1280_OPMODE_TX 0x03
#define SX1280_OPMODE_RX 0x04

// FIFO Boyutu
#define SX1280_FIFO_SIZE 256

// DIO Pinleri (ESP32 ile bağlarken kullanışlı)
#define SX1280_DIO1_DEFAULT 39 // Örnek, kendi bağlantına göre değiştir

// Diğer sabitler
#define SX1280_DEFAULT_FREQ 2400000000UL // 2.4GHz
#define SX1280_DEFAULT_BW 1600000        // 1.6MHz

// Spreading Factor değerleri
#define SX1280_SF5 5
#define SX1280_SF6 6
#define SX1280_SF7 7
#define SX1280_SF8 8
#define SX1280_SF9 9
#define SX1280_SF10 10
#define SX1280_SF11 11
#define SX1280_SF12 12

// Bandwidth değerleri (Hz)
#define SX1280_BW_203125 203125
#define SX1280_BW_406250 406250
#define SX1280_BW_812500 812500
#define SX1280_BW_1625000 1625000

// Code Rate değerleri
#define SX1280_CR_4_5 0x01
#define SX1280_CR_4_6 0x02
#define SX1280_CR_4_7 0x03
#define SX1280_CR_4_8 0x04

// Güç seviyeleri (dBm)
#define SX1280_POWER_MIN -18
#define SX1280_POWER_MAX 13

// Timeout değerleri
#define SX1280_TIMEOUT_NONE 0x0000
#define SX1280_TIMEOUT_MAX 0xFFFF

// Modülasyon türleri
#define SX1280_PACKET_TYPE_LORA 0x01
#define SX1280_PACKET_TYPE_FLRC 0x03
#define SX1280_PACKET_TYPE_BLE 0x05

// IRQ Maskeleri
#define SX1280_IRQ_TX_DONE 0x0001
#define SX1280_IRQ_RX_DONE 0x0002
#define SX1280_IRQ_SYNCWORD_VALID 0x0004
#define SX1280_IRQ_SYNCWORD_ERROR 0x0008
#define SX1280_IRQ_HEADER_VALID 0x0010
#define SX1280_IRQ_HEADER_ERROR 0x0020
#define SX1280_IRQ_CRC_ERROR 0x0040
#define SX1280_IRQ_RANGING_SLAVE_RESPONSE_DONE 0x0080
#define SX1280_IRQ_RANGING_SLAVE_REQUEST_DISCARDED 0x0100
#define SX1280_IRQ_RANGING_MASTER_RESULT_VALID 0x0200
#define SX1280_IRQ_RANGING_MASTER_TIMEOUT 0x0400
#define SX1280_IRQ_RANGING_SLAVE_REQUEST_VALID 0x0800
#define SX1280_IRQ_CAD_DONE 0x1000
#define SX1280_IRQ_CAD_DETECTED 0x2000
#define SX1280_IRQ_RX_TX_TIMEOUT 0x4000
#define SX1280_IRQ_PREAMBLE_DETECTED 0x8000

#endif // SX1280_DEFINITIONS_H